import Foundation
import UIKit

extension UIColor{
    @nonobjc class var grapefruit: UIColor {
        return UIColor(red: 237.0 / 255.0, green: 103.0 / 255.0, blue: 103.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var brownGrey: UIColor {
        return UIColor(white: 158.0 / 255.0, alpha: 1.0)
    }    
}
